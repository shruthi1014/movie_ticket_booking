function ADD1()
{
    var F1= document.getElementById('F1').value;
    F1++;
    document.getElementById("F1").value = F1;
}

function SUB1()
{
    var F1= document.getElementById('F1').value;
    F1--;
    if(F1<0)
    {
        alert("NOT POSSIBLE");
    }
    else
    {
        document.getElementById("F1").value = F1;
    }
}


